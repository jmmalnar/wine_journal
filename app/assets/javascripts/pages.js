$(document).ready(function() {
    $menuLeft = $('.pushmenu-left');
    $title = $('#title');

    $title.click(function() {
        $(this).toggleClass('active');
        $('.pushmenu-push').toggleClass('pushmenu-push-toright');
        $menuLeft.toggleClass('pushmenu-open');
    });
});